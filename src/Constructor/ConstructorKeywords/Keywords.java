/*
   There are two type of key words used only in  constructor they are super,this
   The keyword are must first code of the constructor
   In the constructor you cannot use the both keyword at a time

 */

/*
    Differences between super(),this() and super,this

    The super() and this() only used inside the constructor
    The super() and this() user to call the super class constructor or current class constructor

    The super keyword is used to call the parent class instance variable
    The this keyword is used to call the current class instance variable

 */

